package lab2;

public class Research {
	private String researchcode;
	private String researchtopic;
	private float fundinginfo;
	
	public float getFundinginfo() {
		return fundinginfo;
	}
	public void setFundinginfo(float fundinginfo) {
		this.fundinginfo = fundinginfo;
	}
	public String getResearchtopic() {
		return researchtopic;
	}
	public void setResearchtopic(String researchtopic) {
		this.researchtopic = researchtopic;
	}
	public String getResearchcode() {
		return researchcode;
	}
	public void setResearchcode(String researchcode) {
		this.researchcode = researchcode;
	}
}
